<?php
 
namespace Komputronika\Kalkulator;
 
class Kalkulator {
 
    public function tambah($a, $b) {
        return $a + $b;
    }

    public function kurang($a, $b) {
        return $a - $b;
    }

    public function kali($a, $b) {
        return $a * $b;
    }

    public function bagi($a, $b) {
        return $a / $b;
    }

}
